package POJO;

public class Grade {

    private int grade;
    private String gradeName;
    private int gradeWage;

    public int getGrade() {
        return grade;
    }

    public void setGrade(int grade) {
        this.grade = grade;
    }

    public String getGradeName() {
        return gradeName;
    }

    public void setGradeName(String gradeName) {
        this.gradeName = gradeName;
    }

    public int getGradeWage() {
        return gradeWage;
    }

    public void setGradeWage(int gradeWage) {
        this.gradeWage = gradeWage;
    }
}
